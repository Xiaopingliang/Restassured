public class Forcirculation
{
    public static void main(String[] args){


        int i=0;
        while(i<10){

            System.out.println("The i is"+" "+i);
            i++;
            break;

        }

        for(int j=0;j<10;j++){

            System.out.println("The j is"+" "+j);

        }



    }
}
